package digitaslbi.ext;

import android.content.Context;
import android.util.AttributeSet;

/**
 * Extends the {@link TextExtension} by providing empty implementations for its methods.
 */
public class DefaultTextExtension<T extends android.widget.TextView> extends TextExtension<T> {

    public DefaultTextExtension(Context context, AttributeSet attrs, int defStyleAttr, int defStyleRes) {
        super(context, attrs, defStyleAttr, defStyleRes);
    }

    @Override
    public int getFlag() {
        return -1;
    }
}
